def update_canvas():
    canvas.delete("all")
    for i in range(height):
        for j in range(width):
            x1, y1 = j * cell_size, i * cell_size
            x2, y2 = x1 + cell_size, y1 + cell_size
            if map[i][j] == 1:
                color = "blue"
            elif map[i][j] == 0:
                color = "black"
            elif map[i][j] == 2:
                color = "blue"
            elif map[i][j] == 3:
                color = "pink"
            canvas.create_rectangle(x1, y1, x2, y2, fill=color, outline="gray")

update_canvas()
win.mainloop()

